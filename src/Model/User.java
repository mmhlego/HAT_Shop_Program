package Model;

public class User {
	public String FirstName;
	public String LastName;
	public String Username;
	public String Passeord;
	public String Phone;
	public String Email;

	public User(String firstName, String lastName, String username, String passeord, String phone, String email) {
		super();
		FirstName = firstName;
		LastName = lastName;
		Username = username;
		Passeord = passeord;
		Phone = phone;
		Email = email;
	}

}